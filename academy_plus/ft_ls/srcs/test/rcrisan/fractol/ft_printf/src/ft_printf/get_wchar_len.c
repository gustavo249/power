#include "ft_printf.h"

int	get_wchar_len(wchar_t chr)
{
	if (chr < 0x7F)
		return (1);
	if (chr < 0x7FF)
		return (2);
	if (chr < 0xFFFF)
		return (3);
	return (4);
}
